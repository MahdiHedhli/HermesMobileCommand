class MockAlphaRepository implements AlphaRepository {
  const MockAlphaRepository();

  @override
  Future<HomeAlphaSnapshot> loadHome() async {
    final agents = await loadAgents();
    final missions = await loadMissions();
    final inbox = await loadInbox();
    return HomeAlphaSnapshot(
      stats: const [
        DashboardStat(
            label: 'Agents', value: '12', trend: '9 online', intent: 'neutral'),
        DashboardStat(
            label: 'Online',
            value: '9',
            trend: '+2 since 09:00',
