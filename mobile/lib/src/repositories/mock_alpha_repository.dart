          sender: 'You',
          body:
              'Compare the schema diff first. Do not write until I approve the exact file list.',
          timeLabel: '12:42',
          fromUser: true,
        ),
        AssistanceMessageAlpha(
          sender: 'Repo Sentinel',
          body:
              'Confirmed. I will inspect only and report back with affected paths.',
          timeLabel: '12:42',
          fromUser: false,
        ),
      ],
    );
  }

  @override
  Future<TerminalSessionAlpha> loadTerminalSession(String sessionId) async {
    return TerminalSessionAlpha(
      id: sessionId,
