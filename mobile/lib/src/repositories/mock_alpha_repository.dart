        DashboardStat(
            label: 'Missions',
            value: '4',
            trend: '2 running',
            intent: 'active'),
        DashboardStat(
            label: 'Approvals',
            value: '3',
            trend: '1 critical',
            intent: 'warn'),
        DashboardStat(
            label: 'Notifications',
            value: '18',
            trend: '5 unread',
            intent: 'active'),
        DashboardStat(
            label: 'Security',
            value: '1',
            trend: 'requires review',
            intent: 'critical'),
      ],
      pendingApprovals: _approvals,
      activeMissions: missions
          .where((mission) => mission.state != MissionState.complete)
          .toList(),
      agents: agents,
      activity: _activity,
      notifications:
          inbox.where((item) => item.kind != InboxKind.approval).toList(),
    );
  }

  @override
  Future<List<FleetAgent>> loadAgents() async => _agents;

  @override
  Future<FleetAgent> loadAgent(String agentId) async {
    return _agents.firstWhere((agent) => agent.id == agentId,
        orElse: () => _agents.first);
  }

  @override
  Future<List<MissionSummary>> loadMissions() async => _missions;

  @override
  Future<List<InboxItem>> loadInbox() async => _inbox;

  @override
  Future<ApprovalAlpha> loadApproval(String approvalId) async {
    return _approvals.firstWhere(
      (approval) => approval.id == approvalId,
      orElse: () => _approvals.first,
    );
  }

  @override
  Future<ApprovalAlpha> approveOnce(String approvalId) async {
    final approval = await loadApproval(approvalId);
    return approval.copyWith(state: 'approved');
  }

  @override
  Future<ApprovalAlpha> deny(String approvalId) async {
    final approval = await loadApproval(approvalId);
    return approval.copyWith(state: 'denied');
  }

  @override
  Future<AssistanceSessionAlpha> loadAssistanceSession(String sessionId) async {
    return AssistanceSessionAlpha(
      id: sessionId,
      agentName: 'Repo Sentinel',
      node: 'work-vm-02',
      mission: 'Release branch verification',
      state: AssistanceState.active,
      reason:
          'Agent needs operator guidance before rewriting generated migration files.',
      messages: const [
        AssistanceMessageAlpha(
          sender: 'Repo Sentinel',
